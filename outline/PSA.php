<h1 ALIGN="center">詩篇簡介</h1>
<p>
詩篇是聖經中的第十九卷經書，詩歌智慧書中的第二卷，詩篇是　神子民中間一卷寶貴的詩集，經由多人寫成，又經長期編輯而成。約伯記是帶領人進入　神的學校，在那裡由各種教育而認識　神。詩篇則是帶領人到至聖所，藉著禱告、傾吐、默想、安息、讚美與　神交通。</p>
<p>
詩篇中大部分的作品是大衛所寫，他是　神所膏的一位傑出的詩人，是以色列人中的美歌者(撒下23:1)，在一百五十篇中，屬大衛具名的有七十三篇，不具名的也不少(詩2篇,
徒4:25; 詩95篇, 來4:7; 詩96篇, 代上16:23-33;詩105篇,
代上16:8-23，因此有人稱詩篇為「大衛的詩」或是「大衛的寶庫」。</p>
<p>
